// Helper function to get banners based on category and subcategory
export const getBanners = (category, subcategory = null) => {
    if (subcategory && CATEGORY_BANNERS[category]?.subcategories?.[subcategory]) {
        return CATEGORY_BANNERS[category].subcategories[subcategory];
    }
    return CATEGORY_BANNERS[category] || CATEGORY_BANNERS.accessories; // Default fallback
};
